package sg.edu.rp.c346.id19043996.c347_ps01;

public class Holiday {

    private String festival;
    private Boolean holidayImage;

    public Holiday(String moduleName, Boolean moduleImage) {
        this.festival = festival;
        this.holidayImage = holidayImage;
    }

    public String getFestival() {
        return festival;
    }

    public void setFestival(String moduleName) {
        this.festival = festival;
    }

    public Boolean getHolidayImage() {
        return holidayImage;
    }

    public void setHolidayImage(Boolean moduleImage) {
        this.holidayImage = holidayImage;
    }

}
